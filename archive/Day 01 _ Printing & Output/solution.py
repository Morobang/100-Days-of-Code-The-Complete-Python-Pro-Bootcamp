# Day 01 — Printing & Output — SOLUTIONS

# Exercise 1 — three values on separate lines with one print()
# \n is the newline character — it moves to the next line
print("Rocket\nPretoria\nPython")

# Exercise 2 — sep changes the separator between values
print("Python", "is", "awesome", sep="*")   # → Python*is*awesome

# Exercise 3 — end="" stops print from adding a newline
# so the next print continues on the same line
print("Hello", end=" ")
print("World!")                              # → Hello World!
